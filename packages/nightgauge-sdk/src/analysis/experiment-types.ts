/**
 * Types and Zod schemas for the A/B Testing Framework.
 *
 * Defines experiment configuration, assignment, outcome recording, and
 * report generation types used by ExperimentManager.
 *
 * @see Issue #949 - A/B Testing Framework for Model Routing Decisions
 */

import { z } from "zod";

// ============================================================================
// Schemas
// ============================================================================

/** Model tier schema (matches DefaultModelSchema from config/schema.ts) */
const ModelTierSchema = z.enum(["sonnet", "opus", "haiku", "fable"]);

/** Claude effort schema (matches ClaudeEffort from AutoModelSelector; xhigh for the frontier tier, #73) */
const EffortSchema = z.enum(["low", "medium", "high", "xhigh"]);

/** Experiment group variant */
export const ExperimentGroupSchema = z.enum(["control", "treatment"]);
export type ExperimentGroup = z.infer<typeof ExperimentGroupSchema>;

/**
 * Experiment configuration — stored in `.nightgauge/config.yaml`
 * under `model_routing.experiment`.
 */
export const ExperimentConfigSchema = z.object({
  name: z.string().min(1),
  active: z.boolean().default(false),
  control: z.object({
    model: ModelTierSchema,
    effort: EffortSchema.optional(),
  }),
  treatment: z.object({
    model: ModelTierSchema,
    effort: EffortSchema.optional(),
  }),
  split_percent: z.number().int().min(0).max(100).default(50),
  target_stages: z.array(z.string()).optional(),
  min_runs: z.number().int().min(1).default(20),
  /** Minimum runs per group before evaluation triggers (Issue #1396) */
  observation_window: z.number().int().min(1).default(10),
  /** Minimum success_rate_delta for treatment to graduate (Issue #1396) */
  min_effect_size: z.number().min(0).max(1).default(0.05),
});
export type ExperimentConfig = z.infer<typeof ExperimentConfigSchema>;

/**
 * Assignment result — returned by ExperimentManager.assign()
 */
export const ExperimentAssignmentSchema = z.object({
  experiment_name: z.string(),
  group: ExperimentGroupSchema,
  model: ModelTierSchema,
  effort: EffortSchema.optional(),
  issue_number: z.number().int(),
  stage: z.string(),
  assigned_at: z.string(),
});
export type ExperimentAssignment = z.infer<typeof ExperimentAssignmentSchema>;

/**
 * Outcome record — appended to JSONL after stage completes
 */
export const ExperimentOutcomeSchema = z.object({
  experiment_name: z.string(),
  group: ExperimentGroupSchema,
  issue_number: z.number().int(),
  stage: z.string(),
  model: ModelTierSchema,
  success: z.boolean(),
  cost_usd: z.number(),
  duration_ms: z.number().int(),
  retry_count: z.number().int().min(0),
  recorded_at: z.string(),
});
export type ExperimentOutcome = z.infer<typeof ExperimentOutcomeSchema>;

/**
 * Per-group aggregated metrics for a report
 */
export interface GroupMetrics {
  runs: number;
  successes: number;
  failures: number;
  success_rate: number;
  avg_cost_usd: number;
  avg_duration_ms: number;
  avg_retry_count: number;
  total_cost_usd: number;
}

/**
 * Comparison report — generated by ExperimentManager.generateReport()
 */
export interface ExperimentReport {
  experiment_name: string;
  total_runs: { control: number; treatment: number };
  metrics: {
    control: GroupMetrics;
    treatment: GroupMetrics;
  };
  comparison: {
    cost_savings_percent: number;
    success_rate_delta: number;
    duration_delta_percent: number;
    recommendation: string;
  };
  sufficient_data: boolean;
  generated_at: string;
}

// ============================================================================
// Evaluation Types (Issue #1396)
// ============================================================================

/**
 * Status of experiment auto-evaluation.
 *
 * - `graduated`         — treatment won; experiment deactivated
 * - `rolled_back`       — treatment lost; experiment deactivated
 * - `deferred`          — insufficient data (< observation_window runs per group)
 * - `already_concluded` — conclusion file already exists; no action taken
 */
export type ExperimentEvaluationStatus =
  "graduated" | "rolled_back" | "deferred" | "already_concluded";

/**
 * Pure evaluation result — no I/O side effects.
 *
 * Returned by ExperimentEvaluator.evaluate().
 */
export interface ExperimentEvaluationResult {
  status: ExperimentEvaluationStatus;
  experiment_name: string;
  control_runs: number;
  treatment_runs: number;
  /** treatment.success_rate − control.success_rate */
  success_rate_delta: number;
  treatment_success_rate: number;
  min_effect_size: number;
  observation_window: number;
  /** Human-readable explanation of evaluation outcome */
  rationale: string;
  /** Human-readable next action for the operator */
  recommendation: string;
}

/**
 * Persisted conclusion state written to disk when an experiment concludes.
 *
 * Written by ExperimentEvaluator.writeConclusion() to:
 * `.nightgauge/analysis/experiments/{name}-conclusion.json`
 */
export interface ExperimentConclusion {
  experiment_name: string;
  status: "graduated" | "rolled_back";
  concluded_at: string; // ISO 8601
  control_runs: number;
  treatment_runs: number;
  success_rate_delta: number;
  treatment_success_rate: number;
  rationale: string;
}
