let _itemCounter = 0;

/** Resets the item counter — call in beforeEach when counter-based issue numbers must be deterministic. */
export function resetItemCounter(): void {
  _itemCounter = 0;
}

/**
 * Creates a mock BoardItem with sensible defaults.
 * Counter auto-increments to ensure unique issue numbers.
 */
export function makeMockIssue(overrides: Partial<MockBoardItem> = {}): MockBoardItem {
  _itemCounter++;
  const n = overrides.number ?? _itemCounter * 100;
  return {
    id: `item-${n}`,
    number: n,
    title: `Test Issue #${n}`,
    state: "open",
    status: "Ready",
    priority: "medium",
    size: "M",
    labels: ["type:feature"],
    assignees: [],
    repo: "nightgauge",
    url: `https://github.com/nightgauge/nightgauge/issues/${n}`,
    isEpic: false,
    ...overrides,
  };
}

/**
 * Creates a mock epic BoardItem with sub-issues linked.
 */
export function makeMockEpic(
  epicNumber: number,
  subIssues: Array<{ number: number; title: string; state: string }>
): MockBoardItem {
  return makeMockIssue({
    number: epicNumber,
    title: `Epic #${epicNumber}`,
    labels: ["type:epic"],
    isEpic: true,
    subIssues,
  });
}

/**
 * Creates a board with specified ready-item count plus some in-progress items.
 */
export function makeMockBoard(
  opts: {
    readyCount?: number;
    inProgressCount?: number;
    includeEpic?: boolean;
    includeBlocked?: boolean;
  } = {}
): MockBoardItem[] {
  const { readyCount = 3, inProgressCount = 1, includeEpic = false, includeBlocked = false } = opts;
  const items: MockBoardItem[] = [];

  // Ready items
  for (let i = 0; i < readyCount; i++) {
    items.push(makeMockIssue({ status: "Ready", number: 100 + i }));
  }

  // In-progress items
  for (let i = 0; i < inProgressCount; i++) {
    items.push(makeMockIssue({ status: "In Progress", number: 200 + i }));
  }

  if (includeEpic) {
    const subs = items.slice(0, Math.min(3, readyCount)).map((it) => ({
      number: it.number,
      title: it.title,
      state: it.state,
    }));
    items.push(makeMockEpic(50, subs));
    // Link sub-issues to parent
    subs.forEach((sub) => {
      const item = items.find((it) => it.number === sub.number);
      if (item) {
        item.parentIssueNumber = 50;
        item.parentIssueTitle = "Epic #50";
      }
    });
  }

  if (includeBlocked) {
    // Make item 100 blocked by item 101
    const blocked = items.find((it) => it.number === 100);
    const blocker = items.find((it) => it.number === 101);
    if (blocked && blocker) {
      blocked.blockedBy = [{ number: blocker.number, title: blocker.title, state: "open" }];
      blocker.blocking = [{ number: blocked.number, title: blocked.title, state: "open" }];
    }
  }

  return items;
}

