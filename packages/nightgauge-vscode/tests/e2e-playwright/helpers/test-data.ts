/**
 * test-data.ts — Test fixture builders for E2E tests (Issue #2749).
 *
 * Provides factory functions that produce realistic but minimal mock data
 * matching the actual IPC schema from IpcClientBase.ts. All timestamps are
 * deterministic (fixed epoch offsets from 2026-01-01T00:00:00Z).
 */

import type {
  MockBoardItem,
  MockStatusCounts,
  MockPipelineStatus,
  MockConfigProject,
} from "./ipc-mock.js";

// Fixed base timestamp for deterministic test data
const BASE_EPOCH_MS = new Date("2026-01-01T00:00:00Z").getTime();

// ---------------------------------------------------------------------------
// HTML escaping
// ---------------------------------------------------------------------------

/**
 * Escapes a string for safe inclusion in HTML content and attribute values.
 * Prevents XSS in the test fixture HTML generated by makeDashboardHtml().
 */
function escapeHtml(str: string | number | undefined): string {
  if (str === undefined || str === null) return "";
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

