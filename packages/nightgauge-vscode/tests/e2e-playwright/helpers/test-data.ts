export type PipelineStage =
  | "issue-pickup"
  | "feature-planning"
  | "feature-dev"
  | "feature-validate"
  | "pr-create"
  | "pr-merge";

export function makeMockPipelineStatus(
  opts: {
    issueNumber?: number;
    status?: string;
    stage?: PipelineStage;
    progress?: number;
    error?: string;
  } = {}
): MockPipelineStatus {
  return {
    executionId: `exec-${BASE_EPOCH_MS}`,
    issueNumber: opts.issueNumber ?? 200,
    status: opts.status ?? "running",
    stage: opts.stage ?? "feature-dev",
    progress: opts.progress ?? 0.5,
    error: opts.error,
  };
}

export function makeMockFailedPipelineStatus(
  opts: {
    issueNumber?: number;
    stage?: PipelineStage;
    error?: string;
  } = {}
): MockPipelineStatus {
  return makeMockPipelineStatus({
    ...opts,
    status: "failed",
    error: opts.error ?? "Skill execution timed out",
  });
}

